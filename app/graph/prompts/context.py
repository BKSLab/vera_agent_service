NO_ANSWER_INSTRUCTION = (
    'Поиск по базе знаний не нашёл информации, относящейся к вопросу '
    'пользователя. Честно сообщи об этом и предложи переформулировать '
    'вопрос — не выдумывай ответ.'
)

SEARCH_UNAVAILABLE_INSTRUCTION = (
    'Поиск по базе знаний сейчас технически недоступен. Сообщи об этом '
    'пользователю и предложи обратиться позже — не выдумывай ответ и не '
    'делай вид, что поиск не требовался.'
)


def format_chunks_instruction(chunks: list[dict]) -> str:
    """Форматирует найденные чанки в инструкцию для финальной генерации."""
    parts = []
    for chunk in chunks:
        title = chunk.get('source_title') or chunk.get('synthetic_title') or 'Источник'
        parts.append(f'[{title}]\n{chunk.get("text", "")}')
    joined = '\n\n'.join(parts)
    return (
        'Вот чанки из базы знаний, релевантные вопросу пользователя. '
        f'Сформируй ответ на их основе, обязательно указав источник:\n\n{joined}'
    )
